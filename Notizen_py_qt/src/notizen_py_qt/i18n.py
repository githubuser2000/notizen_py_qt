_CASEFOLD_KEY_LOOKUP: dict[str, dict[str, str]] = {
    language: {key.casefold(): key for key in table}
    for language, table in TRANSLATIONS.items()
}


def legacy_language_key_for_index(index: int) -> str | None:
    """Return the Notizen.NET ``lang_keys`` name for a numeric enum value."""

    if isinstance(index, int) and 0 <= index < len(LEGACY_LANGUAGE_KEYS):
        return LEGACY_LANGUAGE_KEYS[index]
    return None


def legacy_language_index_for_key(key: str) -> int | None:
    """Return the original Notizen.NET ``lang_keys`` enum index."""

    return LEGACY_LANGUAGE_INDEX.get(key)


def legacy_language_values(language: str | None = None) -> tuple[str, ...]:
    """Return translations in the original ``languages.vb`` array order."""

    canonical = resolve_language(language)
    table = TRANSLATIONS.get(canonical, TRANSLATIONS["English"])
    fallback = TRANSLATIONS.get("Deutsch", {})
    return tuple(table.get(key, fallback.get(key, key)) for key in LEGACY_LANGUAGE_KEYS)


def legacy_language_translations(language: str | None = None) -> tuple[str, ...]:
    """Compatibility alias for the original array-order translation API."""

    return legacy_language_values(language)


def resolve_language(choice: str | None = None, *, locale_name: str | None = None) -> str:
    """Return the canonical Notizen.NET language name.

    ``Auto`` follows the old WinForms code: German/French/Spanish/Russian/Chinese
    UI cultures map to their translations, everything else falls back to English.
    """

    raw = (choice or "Auto").strip()
    if raw and raw.lower() not in {"auto", "automatic", "system"}:
        return LANGUAGE_ALIASES.get(raw.casefold(), raw if raw in TRANSLATIONS else "English")

    loc = locale_name
    if loc is None:
        try:
            loc = locale.getlocale()[0] or ""
        except Exception:
            loc = ""
    loc_norm = (loc or "").replace("-", "_").casefold()
    if loc_norm.startswith("de"):
        return "Deutsch"
    if loc_norm.startswith("fr"):
        return "français"
    if loc_norm.startswith("es"):
        return "spanish"
    if loc_norm.startswith("ru"):
        return "russian"
    if loc_norm.startswith("zh"):
        return "Chinese"
    return "English"


def _lookup(table: Mapping[str, str], language: str, key: str) -> str | None:
    value = table.get(key)
    if value:
        return value
    folded = _CASEFOLD_KEY_LOOKUP.get(language, {}).get(key.casefold())
    if folded:
        value = table.get(folded)
        if value:
            return value
    return None


def tr(language: str | None, key: str, default: str | None = None) -> str:
    canonical = resolve_language(language)
    for language_name in (canonical, "Deutsch"):
        table = TRANSLATIONS.get(language_name, {})
        value = _lookup(table, language_name, key)
        if value is not None:
            return value
    return key if default is None else default


def available_languages() -> tuple[tuple[str, str], ...]:
    return DISPLAY_LANGUAGES
