DISPLAY_LANGUAGES: tuple[tuple[str, str], ...] = (
    ("Auto", "Auto"),
    ("Deutsch", "Deutsch"),
    ("English", "English"),
    ("Français", "français"),
    ("Español", "spanish"),
    ("Русский", "russian"),
    ("中文", "Chinese"),
)


def resolve_language(choice: str | None = None, *, locale_name: str | None = None) -> str:
    """Return the canonical Notizen.NET language name.

    ``Auto`` follows the old WinForms code: German/French/Spanish/Russian/Chinese
    UI cultures map to their translations, everything else falls back to English.
    """
    raw = (choice or "Auto").strip()
    if raw and raw.lower() not in {"auto", "automatic", "system"}:
        return LANGUAGE_ALIASES.get(raw.casefold(), raw if raw in TRANSLATIONS else "English")

    loc = locale_name
    if loc is None:
        try:
            loc = locale.getlocale()[0] or ""
        except Exception:
            loc = ""
    loc_norm = (loc or "").replace("-", "_").casefold()
    if loc_norm.startswith("de"):
        return "Deutsch"
    if loc_norm.startswith("fr"):
        return "français"
    if loc_norm.startswith("es"):
        return "spanish"
    if loc_norm.startswith("ru"):
        return "russian"
    if loc_norm.startswith("zh"):
        return "Chinese"
    return "English"


def tr(language: str | None, key: str, default: str | None = None) -> str:
    canonical = resolve_language(language)
    value = TRANSLATIONS.get(canonical, {}).get(key)
    if value is not None and value != "":
        return value
    value = TRANSLATIONS.get("Deutsch", {}).get(key)
    if value is not None and value != "":
        return value
    return key if default is None else default


def available_languages() -> tuple[tuple[str, str], ...]:
    return DISPLAY_LANGUAGES
